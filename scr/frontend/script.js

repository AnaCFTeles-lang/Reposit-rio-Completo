// --- ACESSIBILIDADE (funciona em TODAS as telas) ---
let tamanhoBase = 16;
const btnAmais = document.getElementById('btnAmais');
const btnAmenos = document.getElementById('btnAmenos');
const btnContraste = document.getElementById('btnContraste');

if (btnAmais) {
  btnAmais.addEventListener('click', () => {
    tamanhoBase += 2;
    document.body.style.fontSize = tamanhoBase + 'px';
  });
}
if (btnAmenos) {
  btnAmenos.addEventListener('click', () => {
    tamanhoBase = Math.max(12, tamanhoBase - 2);
    document.body.style.fontSize = tamanhoBase + 'px';
  });
}
if (btnContraste) {
  btnContraste.addEventListener('click', () => {
    document.body.classList.toggle('alto‑contraste');
  });
}

// --- LOGIN E NAVEGAÇÃO (apenas na index.html) ---
const formLogin = document.getElementById('formLogin');
if (formLogin) {
  formLogin.addEventListener('submit', e => {
    e.preventDefault();
    const usuario = document.getElementById('usuario').value.trim();
    const senha = document.getElementById('senha').value.trim();
    const msgErro = document.getElementById('erro');

    if (!usuario || !senha) {
      msgErro.textContent = 'Preencha todos os campos!';
      msgErro.className = 'erro';
    } else {
      msgErro.textContent = 'Aguarde...';
      msgErro.className = '';

      // 💡 LÓGICA SIMULADA: define para qual tela ir
      if (usuario.includes('@aluno.')) {
        window.location.href = 'tela_aluno.html';
      } else if (usuario.includes('@professor.')) {
        window.location.href = 'tela_professor.html';
      } else if (usuario.includes('@admin.')) {
        window.location.href = 'painel_admin.html';
      } else {
        msgErro.textContent = 'Perfil não reconhecido';
        msgErro.className = 'erro';
      }
    }
  });
}

